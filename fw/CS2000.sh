#!/bin/bash
# Configure the CS2000 on the AES42HAT

i2cset -f -y 1 0x4f 0x03 0x01
i2cset -f -y 1 0x4f 0x05 0x01
i2cset -f -y 1 0x4f 0x16 0x10
i2cset -f -y 1 0x4f 0x06 0x00
i2cset -f -y 1 0x4f 0x07 0x27
i2cset -f -y 1 0x4f 0x08 0x52
i2cset -f -y 1 0x4f 0x09 0x54
i2cset -f -y 1 0x4f 0x17 0x10
gpioset -t 0 GPIO12=1
